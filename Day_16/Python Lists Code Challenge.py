# Python Lists Code Challenge

"""
1. Create a list called colors with the values "red", "green", "blue"
2. Print the first item in the list
3. Change the second item to "yellow"
4. Add "purple" to the end of the list using append()
4. Remove "red" from the list using remove()
5. Print the list
"""
# Create a list
colors = ["red", "green", "blue"]
# Print the first item
print(colors[0])
# Change the second item to "yellow"
colors[2] = "yellow"
# Add "purple" to the end
colors.append("purple")
# Remove "red"
colors.remove("red")
# Print the list
print(colors)