# Many Values to Multiple Variables


x , y , m = "dog", "man", "cat"

print(x)
print(y)
print(m)

# One Value to Multiple Variables

b = k = l = "apple"

print(b)
print(k)
print(l)

# Unpack a Collection

fruits =["apple", "banana", "cherry"]

a , B , c = fruits

print(a)
print(B)
print(c)

